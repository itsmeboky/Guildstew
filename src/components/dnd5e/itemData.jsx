// Generate all enchanted versions of weapons and armor
const enchantedItems = [];
allItems.forEach(item => {
  if (item.category === "weapons" || item.category === "armor") {
    ["+1", "+2", "+3"].forEach(bonus => {
      const enchanted = generateEnchantedItem(item, bonus);
      if (enchanted) enchantedItems.push(enchanted);
    });
  }
});

// Combine base items with enchanted versions
export const allItemsWithEnchanted = [...allItems, ...enchantedItems];

// Group items by category for easy filtering
export const itemsByCategory = allItemsWithEnchanted.reduce((acc, item) => {
  if (!acc[item.category]) {
    acc[item.category] = [];
  }
  acc[item.category].push(item);
  return acc;
}, {});

// Export for use in components
export function getItemsByCategory(category) {
  return itemsByCategory[category] || [];
}

export function searchItems(query) {
  const lowerQuery = query.toLowerCase();
  return allItemsWithEnchanted.filter(item => 
    item.name.toLowerCase().includes(lowerQuery) ||
    item.type.toLowerCase().includes(lowerQuery) ||
    item.description.toLowerCase().includes(lowerQuery)
  );
}