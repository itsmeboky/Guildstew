// Enchantment bonus pricing (multiplier of base cost)
export const enchantmentPricing = {
  "+1": { multiplier: 10, bonusGP: 500 },
  "+2": { multiplier: 25, bonusGP: 5000 },
  "+3": { multiplier: 50, bonusGP: 20000 }
};

// Generate enchanted versions of weapons and armor
export function generateEnchantedItem(baseItem, bonus) {
  const pricing = enchantmentPricing[bonus];
  if (!pricing || (baseItem.category !== "weapons" && baseItem.category !== "armor")) {
    return null;
  }

  const baseCost = parseInt(baseItem.cost.replace(/[^\d]/g, "")) || 0;
  const costUnit = baseItem.cost.includes("gp") ? "gp" : baseItem.cost.includes("sp") ? "sp" : "cp";
  let totalCost = baseCost * pricing.multiplier;
  
  if (costUnit === "sp") totalCost = (totalCost / 10) + (pricing.bonusGP / 10);
  else if (costUnit === "cp") totalCost = (totalCost / 100) + (pricing.bonusGP / 100);
  else totalCost += pricing.bonusGP;

  const enchantedItem = {
    ...baseItem,
    name: `${baseItem.name} ${bonus}`,
    cost: `${Math.ceil(totalCost)} gp`,
    category: "magic",
    type: `${baseItem.type} (Enchanted)`,
    description: `${baseItem.description} This item has a ${bonus} enchantment bonus to attack and damage rolls (weapon) or AC (armor).`,
    bonus: bonus
  };

  if (baseItem.damage) {
    enchantedItem.properties = `${baseItem.properties || ""}, ${bonus} to hit and damage`.trim();
  }
  if (baseItem.armorClass) {
    const baseAC = parseInt(baseItem.armorClass.match(/\d+/)?.[0] || 0);
    const bonusNum = parseInt(bonus.replace("+", ""));
    enchantedItem.armorClass = baseItem.armorClass.replace(/\d+/, baseAC + bonusNum);
  }

  return enchantedItem;
}

