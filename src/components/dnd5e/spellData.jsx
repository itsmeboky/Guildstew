const FULL_CASTERS = ["Bard", "Cleric", "Druid", "Sorcerer", "Wizard"];
const HALF_CASTERS = ["Paladin", "Ranger"];

function getCasterContribution(entry) {
  const { class: cls, level, archetype } = entry;

  if (FULL_CASTERS.includes(cls)) {
    return level; // 1:1
  }

  if (HALF_CASTERS.includes(cls)) {
    // PHB: half casters – sum levels, then half, rounded down
    return level / 2;
  }

  // Third casters: only if they actually have the subclass
  if (cls === "Fighter" && archetype === "Eldritch Knight") {
    // No casting until Fighter 3
    const effectiveLevels = Math.max(0, level - 2);
    return effectiveLevels / 3;
  }

  if (cls === "Rogue" && archetype === "Arcane Trickster") {
    const effectiveLevels = Math.max(0, level - 2);
    return effectiveLevels / 3;
  }

  return 0;
}

export function getSpellSlots(className, level, multiclasses = []) {
  const base = {
    cantrips: 0,
    level1: 0, level2: 0, level3: 0, level4: 0, level5: 0,
    level6: 0, level7: 0, level8: 0, level9: 0,
  };

  const allEntries = [
    { class: className, level: level || 1 },
    ...multiclasses
      .filter(mc => mc && mc.class && mc.level)
      .map(mc => ({ class: mc.class, level: mc.level, archetype: mc.archetype })),
  ];

  // 1) Effective caster level (excluding Warlocks)
  let effectiveCasterLevelFloat = 0;

  for (const entry of allEntries) {
    if (entry.class === "Warlock") continue; // handled as Pact Magic
    effectiveCasterLevelFloat += getCasterContribution(entry);
  }

  const effectiveCasterLevel = Math.min(20, Math.floor(effectiveCasterLevelFloat));

  const result = { ...base };

  if (effectiveCasterLevel > 0) {
    // Note: relying on fullCasterTable defined later in file
    const progression = fullCasterTable[effectiveCasterLevel - 1] || [];
    progression.forEach((count, idx) => {
      if (count > 0) {
        const key = `level${idx + 1}`;
        result[key] = count;
      }
    });
  }

  // 2) Cantrips known: sum from all spellcasting classes
  let totalCantrips = 0;
  for (const entry of allEntries) {
    // relying on getCantripsKnown defined later
    totalCantrips += getCantripsKnown(entry.class, entry.level);
  }
  result.cantrips = totalCantrips;

  return result;
}

