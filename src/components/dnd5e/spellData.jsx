// Convert registry's { level: [slots...] } into a 0-indexed array of
// arrays so the existing [level - 1] indexing still works.
const fullCasterTable = Array.from({ length: 20 }, (_, i) => _FULL_SLOTS[i + 1] || []);
const halfCasterTable = Array.from({ length: 20 }, (_, i) => _halfCasterSlots(i + 1));
const warlockTable = Array.from({ length: 20 }, (_, i) => {
  const pact = _PACT_SLOTS[i + 1] || { slots: 0, level: 0 };
  return { count: pact.slots, level: pact.level };
});

// Sorcerer Cantrips Known: 1-3: 4, 4-9: 5, 10+: 6
// Bard/Druid/Cleric/Wizard/Warlock Cantrips Known differ slightly but 2-4 is average.
// For simplicity/safety we'll use a generous mapping or specific class logic.
function getCantripsKnown(className, level) {
  if (className === 'Sorcerer') {
    return level >= 10 ? 6 : level >= 4 ? 5 : 4;
  }
  if (['Bard', 'Druid', 'Warlock'].includes(className)) {
    return level >= 10 ? 4 : level >= 4 ? 3 : 2;
  }
  if (['Cleric', 'Wizard'].includes(className)) {
    return level >= 10 ? 5 : level >= 4 ? 4 : 3;
  }
  // Artificer is 2, then 3 at 10, 4 at 14. 
  return 0;
}

function getSingleClassSpellSlots(className, level) {
  const slots = { cantrips: 0 };
  // Initialize levels 1-9
  for(let i=1; i<=9; i++) slots[`level${i}`] = 0;

  if (["Fighter", "Barbarian", "Rogue", "Monk"].includes(className)) {
    // TODO: Eldritch Knight / Arcane Trickster
    return slots;
  }
  
  if (["Paladin", "Ranger"].includes(className)) {
    const progression = halfCasterTable[Math.min(level, 20) - 1] || [];
    progression.forEach((count, idx) => {
      if (count > 0) slots[`level${idx + 1}`] = count;
    });
    return slots;
  }
  
  if (className === "Warlock") {
    slots.cantrips = getCantripsKnown(className, level);
    const { count, level: slotLevel } = warlockTable[Math.min(level, 20) - 1] || { count: 0, level: 0 };
    if (count > 0) {
      // Warlocks slots are all of 'slotLevel'
      // But UI might expect them to be in that specific level key?
      // Standard slots logic sums them up. 
      // For warlock, let's just put them in the correct level key.
      slots[`level${slotLevel}`] = count;
    }
    return slots;
  }

  const fullCasters = ["Bard", "Cleric", "Druid", "Sorcerer", "Wizard"];
  if (fullCasters.includes(className)) {
    slots.cantrips = getCantripsKnown(className, level);
    const progression = fullCasterTable[Math.min(level, 20) - 1] || [];
    progression.forEach((count, idx) => {
      if (count > 0) slots[`level${idx + 1}`] = count;
    });
    return slots;
  }
  
  return slots;
}

export function getAllAvailableSpells(primaryClass, multiclasses = [], fullSpellsList = null) {
  if (fullSpellsList && Array.isArray(fullSpellsList) && fullSpellsList.length > 0) {
    const allSpells = {
      cantrips: [],
      level1: [], level2: [], level3: [], level4: [], level5: [],
      level6: [], level7: [], level8: [], level9: []
    };

    const classes = [primaryClass, ...multiclasses.map(mc => mc.class)].filter(Boolean);

    fullSpellsList.forEach(spell => {
      const spellClasses = spell.classes || [];
      const belongsToClass = spellClasses.some(cls => classes.includes(cls));
      if (!belongsToClass) return;

      let levelKey;

      // Open5e: level "0" for cantrips
      if (spell.level === "0" || spell.level === 0 || spell.level === "Cantrip") {
        levelKey = "cantrips";
      } else {
        const numericLevel =
          typeof spell.level === "number"
            ? spell.level
            : parseInt(spell.level, 10);

        if (!numericLevel || numericLevel < 1 || numericLevel > 9) return;
        levelKey = `level${numericLevel}`;
      }

      if (allSpells[levelKey]) {
        allSpells[levelKey].push(spell.name);
      }
    });

    Object.keys(allSpells).forEach(key => {
      allSpells[key].sort();
    });

    return allSpells;
  }

  // fallback: hardcoded cantrips + 1st-level only
  let allSpells = { cantrips: [], level1: [] };
  
  const primarySpells = spellsByClass[primaryClass] || { cantrips: [], level1: [] };
  allSpells.cantrips = [...primarySpells.cantrips];
  allSpells.level1 = [...primarySpells.level1];
  
  multiclasses.forEach(mc => {
    if (mc.class && mc.level >= 1) {
      const mcSpells = spellsByClass[mc.class] || { cantrips: [], level1: [] };
      allSpells.cantrips = [...new Set([...allSpells.cantrips, ...mcSpells.cantrips])];
      allSpells.level1 = [...new Set([...allSpells.level1, ...mcSpells.level1])];
    }
  });
  
  return allSpells;
}

export function getPactSlots(primaryClass, level, multiclasses = []) {
  const allEntries = [
    { class: primaryClass, level },
    ...multiclasses.filter(mc => mc && mc.class && mc.level),
  ];

  const warlockEntries = allEntries.filter(e => e.class === "Warlock");
  if (warlockEntries.length === 0) return null;

  const totalWarlockLevels = warlockEntries.reduce((sum, e) => sum + e.level, 0);
  const cappedLevel = Math.min(20, totalWarlockLevels);

  const { count, level: slotLevel } =
    warlockTable[cappedLevel - 1] || { count: 0, level: 0 };

  if (!count || !slotLevel) return null;

  const cantrips = getCantripsKnown("Warlock", totalWarlockLevels);

  return {
    slotLevel,
    slots: count,
    cantrips,
    totalWarlockLevels,
  };
}

function getMaxSpellLevelThirdCaster(level) {
  // PHB: Eldritch Knight / Arcane Trickster max spell levels
  if (level >= 19) return 4;
  if (level >= 13) return 3;
  if (level >= 7) return 2;
  if (level >= 3) return 1;
  return 0;
}

function getMaxSpellLevelForSingleClass(entry) {
  const { class: cls, level, archetype } = entry;

  if (!level || level <= 0) return 0;

  // Full casters: Bard, Cleric, Druid, Sorcerer, Wizard
  if (["Bard", "Cleric", "Druid", "Sorcerer", "Wizard"].includes(cls)) {
    const progression = fullCasterTable[Math.min(level, 20) - 1] || [];
    let max = 0;
    progression.forEach((count, idx) => {
      if (count > 0) max = idx + 1; // index 0 => 1st level, etc.
    });
    return max;
  }

  // Half casters: Paladin, Ranger
  if (["Paladin", "Ranger"].includes(cls)) {
    const progression = halfCasterTable[Math.min(level, 20) - 1] || [];
    let max = 0;
    progression.forEach((count, idx) => {
      if (count > 0) max = idx + 1;
    });
    return max;
  }

  // Warlock: pact slot level determines the highest normal spell level known
  if (cls === "Warlock") {
    const pact = warlockTable[Math.min(level, 20) - 1];
    return pact?.level || 0;
  }

  // Third casters: Fighter (Eldritch Knight), Rogue (Arcane Trickster)
  if (cls === "Fighter" && archetype === "Eldritch Knight") {
    return getMaxSpellLevelThirdCaster(level);
  }
  if (cls === "Rogue" && archetype === "Arcane Trickster") {
    return getMaxSpellLevelThirdCaster(level);
  }

  return 0;
}

export function getMaxSpellLevelForCharacter(primaryClass, level, multiclasses = []) {
  const allEntries = [
    { class: primaryClass, level },
    ...multiclasses.filter(mc => mc && mc.class && mc.level).map(mc => ({ class: mc.class, level: mc.level, archetype: mc.archetype })),
  ];

  let max = 0;
  for (const entry of allEntries) {
    const entryMax = getMaxSpellLevelForSingleClass(entry);
    if (entryMax > max) max = entryMax;
  }
  return max;
}

export function getCharacterSpellSlots(character) {
  if (!character) return {};

  // Handle Monsters/NPCs: Check for explicit spell_slots property
  if (character.type === 'monster' || character.type === 'npc') {
    if (character.spell_slots) return character.spell_slots;
    if (character.stats?.spell_slots) return character.stats.spell_slots;
    
    // Try to parse "Spellcasting" or "Innate Spellcasting" trait
    const traits = [
      ...(character.special_abilities || []), 
      ...(character.stats?.special_abilities || []), 
      ...(character.traits || []),
      ...(character.abilities || []),
      ...(character.stats?.abilities || [])
    ];
    
    const spellcastingTrait = traits.find(t => {
      const name = (t.name || "").toLowerCase();
      return name.includes("spellcasting");
    });
    
    if (spellcastingTrait) {
      const slots = {};
      const desc = spellcastingTrait.desc || spellcastingTrait.description || "";
      
      // Regex to find "Xth level (Y slots)" patterns
      // e.g. "1st level (4 slots):", "2nd level (3 slots):"
      // Improved to handle "1st-level", "Level 1", etc.
      const levelRegex = /(\d+)(?:st|nd|rd|th)?[\s-]level\s+\((\d+)\s+slots?\)/gi;
      let match;
      while ((match = levelRegex.exec(desc)) !== null) {
        slots[parseInt(match[1])] = parseInt(match[2]);
      }

      if (Object.keys(slots).length === 0) {
         const simpleRegex = /Level\s+(\d+):\s*(\d+)\s*slots?/gi;
         let simpleMatch;
         while ((simpleMatch = simpleRegex.exec(desc)) !== null) {
            slots[parseInt(simpleMatch[1])] = parseInt(simpleMatch[2]);
         }
      }

      // Fallback for simpler format "Level X: Y slots" or similar
      if (Object.keys(slots).length === 0) {
         const simpleRegex = /Level\s+(\d+):\s*(\d+)\s*slots?/gi;
         let simpleMatch;
         while ((simpleMatch = simpleRegex.exec(desc)) !== null) {
            slots[parseInt(simpleMatch[1])] = parseInt(simpleMatch[2]);
         }
      }
      
      // If no slots found but it's a spellcaster with a level, assume standard progression
      if (Object.keys(slots).length === 0) {
        // Try to find "Xth-level spellcaster" with flexible matching
        const levelMatch = desc.match(/(\d+)(?:st|nd|rd|th)?[\s-]level spellcaster/i);
        if (levelMatch) {
          const level = parseInt(levelMatch[1]);
          // Default to full caster progression
          const fullCasterTable = [
            [2], [3], [4, 2], [4, 3], [4, 3, 2], [4, 3, 3], [4, 3, 3, 1], [4, 3, 3, 2], [4, 3, 3, 3, 1],
            [4, 3, 3, 3, 2], [4, 3, 3, 3, 2, 1], [4, 3, 3, 3, 2, 1], [4, 3, 3, 3, 2, 1, 1], [4, 3, 3, 3, 2, 1, 1],
            [4, 3, 3, 3, 2, 1, 1, 1], [4, 3, 3, 3, 2, 1, 1, 1], [4, 3, 3, 3, 2, 1, 1, 1, 1], [4, 3, 3, 3, 3, 1, 1, 1, 1],
            [4, 3, 3, 3, 3, 2, 1, 1, 1], [4, 3, 3, 3, 3, 2, 2, 1, 1]
          ];
          const progression = fullCasterTable[Math.min(level, 20) - 1] || [];
          progression.forEach((count, idx) => {
            if (count > 0) slots[idx + 1] = count;
          });
        }
      }
      return slots;
    }
    
    // Fallback: if no trait but has high level, maybe give slots? 
    // Safer to return empty if we can't confirm.
    if (!character.class) return {};
  }

  // Handle PC Classes
  const className = character.class;
  const level = parseInt(character.level || character.stats?.level || 1);
  
  // Full Caster Progression Table (Bard, Cleric, Druid, Sorcerer, Wizard)
  // Level: [1st, 2nd, 3rd, 4th, 5th, 6th, 7th, 8th, 9th]
  

  const slots = {};

  if (["Bard", "Cleric", "Druid", "Sorcerer", "Wizard"].includes(className)) {
    const progression = fullCasterTable[Math.min(level, 20) - 1] || [];
    progression.forEach((count, idx) => {
      if (count > 0) slots[idx + 1] = count;
    });
  } else if (["Paladin", "Ranger"].includes(className)) {
    const progression = halfCasterTable[Math.min(level, 20) - 1] || [];
    progression.forEach((count, idx) => {
      if (count > 0) slots[idx + 1] = count;
    });
  } else if (className === "Warlock") {
    const { count, level: slotLevel } = warlockTable[Math.min(level, 20) - 1] || { count: 0, level: 0 };
    if (count > 0) {
      // Warlocks have 'count' slots of 'slotLevel' level.
      // But UI expects e.g. { 1: 0, 2: 0, 3: 2 } for 2 level 3 slots? 
      // The UI iterates levels. If we only return {5: 2}, it will show level 5 slots.
      // That's correct for Warlock (they don't have lower level slots usable, they upcast automatically).
      // But maybe user wants to see they exist?
      // Usually Warlock slots are special. Let's just return { [slotLevel]: count }.
      slots[slotLevel] = count;
    }
  } 
  // TODO: Artificer, Arcane Trickster, Eldritch Knight (1/3 casters)

  return slots;
}